function calcularPromedio(num1, num2, num3) {
    const suma = num1 + num2 + num3; // Sumar los tres n√∫meros
    const promedio = suma / 3; // Calcular el promedio
    return promedio; // Retornar el promedio
}

// Ejemplo de uso
const resultado = calcularPromedio(10, 20, 30);
console.log("El promedio es:", resultado); // Salida: El promedio es: 20
